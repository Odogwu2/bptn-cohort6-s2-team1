package com.bptn.course._01_variables;

public class ASCIIExample {

	public static void main(String[] args) {
		
		//Step 1: Creating a character type variable 
		char myChar = 'B';
		
		//Step 2: Get the ASCII code of myChar
		int myAscii = myChar; 
		
		System.out.println ("The ASCII value of " + myChar + " is " + myAscii);

	}

}
